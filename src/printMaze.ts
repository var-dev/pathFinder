const WIDTH = 71
const HEIGHT = 19

function coordinateToIndex(x: number, y: number) {
  return y * WIDTH + x
} 

multiplePaths.forEach((point) => {
  let {x, y, status} = point
  const index = coordinateToIndex(x, y)
  maze[index] = status === 'N' ? '.' : status
  maze[coordinateToIndex(1,1)] = 'S'
})

const output = maze.reduce((result: string, char: string, index) => {
  if ((index % WIDTH === 0) && index > 0) {
    char = '\n' + char
  }
  return result + char
},'')

console.log(output)
